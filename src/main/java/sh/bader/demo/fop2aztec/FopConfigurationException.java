package sh.bader.demo.fop2aztec;

public class FopConfigurationException extends Exception {
    public FopConfigurationException() {
        super();
    }

    public FopConfigurationException(String message) {
        super(message);
    }

    public FopConfigurationException(Throwable cause) {
        super(cause);
    }

    public FopConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }
}
